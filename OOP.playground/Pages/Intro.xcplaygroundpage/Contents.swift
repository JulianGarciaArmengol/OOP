 # Introduccion
 
 ## ¿Qué es la programación orientada a objetos (OOP)?
 
 Es un paradigma de la programación que se basa en el concepto de "objetos" (usualmente clases o estructuras), los cuales pueden contener propiedades y métodos. Se enfoca en organizar el software en piezas reutilizables y manejables.
 
 Tiene cuatro pilares fundamentales: **Herencia, Polimorfismo, Abstracción, Encapsulamiento**.
 
